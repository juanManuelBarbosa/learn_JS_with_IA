//1. Números (Number): Representan valores numéricos, ya sean enteros o decimales.
//Por ejemplo: 10, 3.14, -5. 

let numero = 2;
 
//2. Cadenas de texto (String): Representan secuencias de caracteres entre comillas simples 
//o dobles. Por ejemplo: "Hola", 'JavaScript', "123". 

let string = "Hola , soy una cadena de texto de tipo 'string' ";

//3. Booleanos (Boolean): Representan valores de verdadero (true) o falso (false).
//Se utilizan para evaluar condiciones lógicas. Por ejemplo: true, false. 

let boolean = true ; 
 
//4. Objetos (Object): Representan entidades del mundo real y contienen propiedades y métodos.
//Los objetos se definen con llaves {}. Por ejemplo: {nombre: "Juan", edad: 25}. 
 
let objeto = {
    nombre : "objeto",
    tipo: "objet",
    caracteristica:"pos soy un objeto"
};

//5. Arreglos (Array): Representan colecciones ordenadas de elementos. Los elementos se 
//almacenan en corchetes [] y se separan por comas. Por ejemplo: [1, 2, 3, 4]. 
 
let arreglo = [1, "elemento" , true];

//6. Nulos (Null): Representa la ausencia de valor. Se utiliza para indicar que una variable 
//no tiene ningún valor asignado intencionalmente. Por ejemplo: null. 

null; 
 
//7. Indefinido (Undefined): Representa una variable que ha sido declarada pero no
//se le ha asignado ningún valor. Por ejemplo: let x; 
 
let undefined; 

//8. Símbolo (Symbol): Es un tipo de dato introducido en ECMAScript 6.
//Representa un valor único e inmutable que se utiliza como identificador único en objetos. 
 
