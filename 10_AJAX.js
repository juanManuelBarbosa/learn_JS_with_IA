/*AJAX (Asynchronous JavaScript And XML) es una técnica de programación que permite 
realizar solicitudes asíncronas al servidor desde una página web sin necesidad de 
recargarla por completo. En lugar de enviar una solicitud tradicional y esperar una 
respuesta completa, AJAX permite enviar y recibir datos en segundo plano, lo que permite 
actualizar partes específicas de una página web sin interrumpir la experiencia del usuario. 
 
La principal ventaja de AJAX es su capacidad para realizar solicitudes asíncronas, 
lo que significa que la página web puede continuar ejecutando otras tareas mientras 
espera la respuesta del servidor. Esto mejora la capacidad de respuesta y la experiencia 
del usuario, ya que no hay interrupciones visibles durante el proceso de carga. 
 
Aunque el nombre sugiere que AJAX se basa en XML, en realidad se puede utilizar con 
cualquier formato de datos, como JSON. De hecho, JSON se ha vuelto más popular en la 
actualidad debido a su formato más ligero y fácil de trabajar. 
 
Para implementar AJAX en una página web, se utiliza JavaScript para realizar la 
solicitud al servidor utilizando la función  XMLHttpRequest  o la API de Fetch en 
versiones más recientes de JavaScript. Una vez que se obtiene la respuesta del servidor, 
se pueden realizar acciones adicionales, como actualizar el contenido de la página, 
mostrar mensajes de error o realizar otras operaciones en función de los datos recibidos. 
 
En resumen, AJAX es una técnica de programación que permite realizar solicitudes 
asíncronas al servidor desde una página web, lo que mejora la capacidad de respuesta 
y la experiencia del usuario al permitir actualizaciones parciales de contenido sin 
recargar la página completa. */

